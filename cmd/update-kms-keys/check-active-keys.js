async function checkKeys() {
  for (const address of addresses) {
    try {
      const account = await fcl.account(address);
      const activeKeys = account.keys.filter((k) => !k.revoked);

      if (activeKeys.length === 1) {
        console.log(` ${address} has exactly 1 active key.`);
      } else {
        console.log(` ${address} has ${activeKeys.length} active keys.`);
      }
    } catch (err) {
      console.error(` Error checking ${address}: ${err.message}`);
    }
  }
}

async function checkKeyIndexes() {
  for (const address of addresses) {
    try {
      const account = await fcl.account(address);
      const activeKeys = account.keys.filter((k) => !k.revoked);

      if (activeKeys.length === 1) {
        let index = activeKeys[0].index;
        if (index != 1) {
          console.log("INDEX: ", activeKeys[0].index, "ADDRESS: ", address);
        }
      } else {
        console.log(` ${address} has ${activeKeys.length} active keys.`);
      }
    } catch (err) {
      console.error(` Error checking ${address}: ${err.message}`);
    }
  }
}

async function compare_db_keys_with_chain_keys() {
  const addresses_to_pks = get_addresses_to_pks();
  for (const [address, pk] of Object.entries(addresses_to_pks)) {
    try {
      const chain_account = await fcl.account(address);
      const activeKeys = chain_account.keys.filter((k) => !k.revoked);
      const dbKey = pk.replace(/^0x/, "");

      if (activeKeys.length === 1) {
        const chainKey = activeKeys[0].publicKey;
        if (chainKey !== dbKey) {
          console.log(
            ` Key mismatch for ${address}: DB key ${dbKey} vs Chain key ${chainKey}`
          );
        } else {
          console.log(` Key match for ${address}: ${dbKey}`);
        }
      } else {
        console.log(` ${address} has ${activeKeys.length} active keys.`);
      }
    } catch (err) {
      console.error(` Error checking ${address}: ${err.message}`);
    }
  }
}

